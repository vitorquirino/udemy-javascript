let n1 = parseInt(prompt('Digite um numero'));
let n2 = parseInt(prompt('Digite outro numero'));

const resultadoModulo = (n1 % n2)
const resultadoElevado = (n1 ** n2)

alert('o resto da divisão de ' + n1 + ' por '+ n2 + ' é: ' + resultadoModulo);
alert('o numero ' + n1 + ' eleveado a ' + n2 + ' é: ' + resultadoElevado);